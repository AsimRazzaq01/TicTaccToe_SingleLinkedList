public class Node {
    String data;  // Could be 'X', 'O', or empty
    Node next;

    // Constructor for a new node
    public Node(String data) {
        this.data = data;
        this.next = next;
    }

    // get Data Method
    public String getData() {
        return data;
    }
    // set Data Method
    public void setData(String data) {
        this.data = data;
    }
}



